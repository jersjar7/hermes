// lib/core/presentation/constants/spacing.dart

/// Standardized spacing values used throughout the Hermes app.
/// Based on an 8-point grid system for consistency.
class HermesSpacing {
  // Prevent instantiation
  HermesSpacing._();

  /// 4.0 - Extra small spacing (e.g., between icon and label)
  static const double xs = 4.0;

  /// 8.0 - Small spacing (e.g., padding inside buttons)
  static const double sm = 8.0;

  /// 16.0 - Medium spacing (e.g., standard padding)
  static const double md = 16.0;

  /// 24.0 - Large spacing (e.g., between sections)
  static const double lg = 24.0;

  /// 32.0 - Extra large spacing (e.g., page margins)
  static const double xl = 32.0;

  /// 48.0 - XXL spacing (e.g., hero sections)
  static const double xxl = 48.0;

  /// Standard page padding
  static const double pagePadding = md;

  /// Standard card padding
  static const double cardPadding = md;

  /// Button internal padding
  static const double buttonPadding = sm;
}
